def diagonalDifference(arr):
    # Write your code here
    l_r = 0
    r_l = 0
    l = len(arr[0]) - 1
    j = 0
    for i in arr:
        l_r += i[j]
        r_l += i[l - j]
        j += 1
    return(abs(l_r - r_l))

    
